#!/usr/bin/env node
import { access, chmod, mkdir, readFile, writeFile } from "node:fs/promises";
import { constants as fsConstants } from "node:fs";
import { spawnSync } from "node:child_process";
import crypto from "node:crypto";
import path from "node:path";
import readline from "node:readline/promises";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const APP_ROOT = path.resolve(__dirname, "..");
const DEFAULT_INSTANCES_ROOT = path.join(APP_ROOT, "runtime");
const DEFAULT_HERMES_AGENT_SRC = path.join(APP_ROOT, "vendor", "hermes-agent");
const CONSOLE_ENV = path.join(APP_ROOT, ".env");

const args = new Set(process.argv.slice(2));
const skipInstall = args.has("--skip-install");
const forceEnv = args.has("--force-env");

function displayPath(value) {
  const text = String(value || "");
  if (!text) return "";
  const relative = path.relative(APP_ROOT, text);
  if (relative && !relative.startsWith("..") && !path.isAbsolute(relative)) return relative === "" ? "." : relative;
  if (text.startsWith(process.env.HOME || "\0")) return `~${text.slice((process.env.HOME || "").length)}`;
  return path.basename(text) || text;
}

async function exists(file) {
  try {
    await access(file, fsConstants.F_OK);
    return true;
  } catch {
    return false;
  }
}

async function executable(file) {
  try {
    await access(file, fsConstants.X_OK);
    return true;
  } catch {
    return false;
  }
}

function parseEnv(text) {
  const env = {};
  for (const rawLine of String(text || "").split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#") || !line.includes("=")) continue;
    const [key, ...rest] = line.split("=");
    let value = rest.join("=").trim();
    if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1);
    }
    env[key.trim()] = value;
  }
  return env;
}

async function loadEnvFile(file) {
  if (!(await exists(file))) return {};
  return parseEnv(await readFile(file, "utf8"));
}

function envFlag(value) {
  return ["1", "true", "yes", "on"].includes(String(value || "").trim().toLowerCase());
}

function isLoopbackHost(host) {
  const normalized = String(host || "").trim().toLowerCase().replace(/^\[|\]$/g, "");
  return normalized === "" || normalized === "localhost" || normalized === "::1" || normalized.startsWith("127.");
}

async function promptConsoleToken(reason) {
  if (!process.stdin.isTTY || !process.stdout.isTTY) return crypto.randomBytes(24).toString("base64url");
  console.log("");
  console.log(reason);
  console.log("Enter a console token to save in .env, or press Enter to generate one automatically.");
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
  try {
    const answer = await rl.question("HERMES_CONSOLE_TOKEN: ");
    return answer.trim() || crypto.randomBytes(24).toString("base64url");
  } finally {
    rl.close();
  }
}

async function upsertEnvValue(file, key, value) {
  const text = await exists(file) ? await readFile(file, "utf8") : "";
  const lines = text.split(/\r?\n/);
  let changed = false;
  const next = lines.map((line) => {
    if (!line.startsWith(`${key}=`)) return line;
    changed = true;
    return `${key}=${value}`;
  });
  if (!changed) next.push(`${key}=${value}`);
  await writeFile(file, `${next.filter((line, index) => line || index < next.length - 1).join("\n").replace(/\n*$/, "")}\n`, { mode: 0o600 });
}

