function run(command, commandArgs, options = {}) {
  const result = spawnSync(command, commandArgs, {
    cwd: options.cwd || APP_ROOT,
    env: process.env,
    encoding: "utf8",
    stdio: options.stdio || "pipe",
  });
  return {
    ok: result.status === 0,
    status: result.status,
    stdout: result.stdout || "",
    stderr: result.stderr || "",
    error: result.error || null,
  };
}

function commandAvailable(command) {
  const result = spawnSync("/bin/sh", ["-lc", `command -v '${String(command).replace(/'/g, "'\\''")}'`], {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  });
  return result.status === 0 && Boolean(String(result.stdout || "").trim());
}

function line(status, message) {
  console.log(`[${status}] ${message}`);
}

function resolveProjectPath(value) {
  if (!value) return value;
  if (value === "~") return process.env.HOME || value;
  if (value.startsWith("~/")) return path.join(process.env.HOME || "", value.slice(2));
  if (path.isAbsolute(value)) return value;
  return path.resolve(APP_ROOT, value);
}

async function maybeChmod(file) {
  if (!(await exists(file))) return false;
  if (await executable(file)) return true;
  await chmod(file, 0o755);
  return true;
}

async function maybeCloneHermesAgent(target, env) {
  if (await exists(path.join(target, ".git")) || await exists(path.join(target, "Dockerfile")) || await exists(path.join(target, "pyproject.toml")) || await exists(path.join(target, "package.json"))) {
    line("ok", `Hermes source ready: ${displayPath(target)}`);
    return;
  }
  if (env.HERMES_AGENT_AUTO_CLONE === "0" || env.HERMES_AGENT_AUTO_CLONE === "false") {
    line("warn", "Hermes source missing; auto clone disabled");
    return;
  }
  const repo = String(env.HERMES_AGENT_REPO_URL || "https://github.com/NousResearch/hermes-agent.git").trim();
  if (!repo) {
    line("warn", "Hermes source missing; HERMES_AGENT_REPO_URL is empty");
    return;
  }
  if (!commandAvailable("git")) {
    line("warn", "Hermes source missing; git is not installed");
    return;
  }
  await mkdir(path.dirname(target), { recursive: true });
  line("run", `git clone ${repo} ${displayPath(target)}`);
  const clone = run("git", ["clone", "--depth", "1", repo, target], { stdio: "inherit" });
  if (!clone.ok) {
    line("warn", "Hermes source clone failed; set HERMES_AGENT_SRC or HERMES_AGENT_REPO_URL in .env");
  }
}

async function main() {
  const rootEnv = await loadEnvFile(path.join(DEFAULT_INSTANCES_ROOT, ".env"));
  const consoleEnv = await loadEnvFile(CONSOLE_ENV);
  const env = { ...process.env, ...rootEnv, ...consoleEnv };

  const instancesRoot = resolveProjectPath(env.HERMES_INSTANCES_ROOT || DEFAULT_INSTANCES_ROOT);
  const hermesDockerBin = resolveProjectPath(env.HERMES_DOCKER_BIN || path.join(APP_ROOT, "bin", "hermes-docker"));
  const hermesConsoleBin = resolveProjectPath(env.HERMES_CONSOLE_BIN || path.join(APP_ROOT, "bin", "hermes-console"));
  const hermesAgentSrc = resolveProjectPath(env.HERMES_AGENT_SRC || DEFAULT_HERMES_AGENT_SRC);
  const dataDir = resolveProjectPath(env.HERMES_CONSOLE_DATA_DIR || path.join(APP_ROOT, "data"));
  const dbDir = path.dirname(resolveProjectPath(env.HERMES_CONSOLE_DB || path.join(dataDir, "fleet.db")));
  const logsDir = path.join(APP_ROOT, "logs");
  const secretsDir = path.join(APP_ROOT, "secrets");

  console.log("Hermes Fleet Console baseline setup");
  console.log(`App root: ${displayPath(APP_ROOT)}`);
  console.log("");

  await mkdir(instancesRoot, { recursive: true });
  line("ok", `instances root ready: ${displayPath(instancesRoot)}`);

  await mkdir(path.join(APP_ROOT, "bin"), { recursive: true });
  line("ok", "project bin directory ready: bin");

  await mkdir(path.join(APP_ROOT, "vendor"), { recursive: true });
  line("ok", "vendor directory ready: vendor");

  await mkdir(secretsDir, { recursive: true, mode: 0o700 });
  line("ok", `secrets directory ready: ${displayPath(secretsDir)}`);

  await mkdir(dataDir, { recursive: true });
  line("ok", `console data directory ready: ${displayPath(dataDir)}`);

  await mkdir(dbDir, { recursive: true });
  line("ok", `console DB directory ready: ${displayPath(dbDir)}`);

  await mkdir(logsDir, { recursive: true });
  line("ok", `console logs directory ready: ${displayPath(logsDir)}`);

  if (forceEnv || !(await exists(CONSOLE_ENV))) {
    await writeFile(CONSOLE_ENV, envTemplate({ instancesRoot, hermesDockerBin, hermesAgentSrc }), { mode: 0o600 });
    line("ok", `${forceEnv ? "rewrote" : "created"} console env: .env`);
  } else {
    line("ok", "console env already exists: .env");
  }

  const currentEnv = { ...env, ...(await loadEnvFile(CONSOLE_ENV)) };
  const consoleHost = currentEnv.HERMES_CONSOLE_HOST || "127.0.0.1";
  if (!isLoopbackHost(consoleHost) && !currentEnv.HERMES_CONSOLE_TOKEN) {
    const token = await promptConsoleToken(`Console host is ${consoleHost}, so API access needs a token.`);
    await upsertEnvValue(CONSOLE_ENV, "HERMES_CONSOLE_TOKEN", token);
    await upsertEnvValue(CONSOLE_ENV, "HERMES_CONSOLE_REQUIRE_AUTH", "1");
    line("ok", "saved console token for LAN binding");
  } else if (envFlag(currentEnv.HERMES_CONSOLE_REQUIRE_AUTH) && !currentEnv.HERMES_CONSOLE_TOKEN) {
    const token = await promptConsoleToken("Console auth is required, but HERMES_CONSOLE_TOKEN is empty.");
    await upsertEnvValue(CONSOLE_ENV, "HERMES_CONSOLE_TOKEN", token);
    line("ok", "saved console token for required auth");
  }

  if (await maybeChmod(hermesDockerBin)) {
    line("ok", `hermes-docker executable: ${displayPath(hermesDockerBin)}`);
  } else {
    line("missing", "hermes-docker not found in bin");
  }

  if (await maybeChmod(hermesConsoleBin)) {
    line("ok", `hermes-console executable: ${displayPath(hermesConsoleBin)}`);
  } else {
    line("warn", "hermes-console not found in bin");
  }

  if (!(await exists(path.join(APP_ROOT, "node_modules")))) {
    if (skipInstall) {
      line("warn", "node_modules missing; skipped npm install because --skip-install was provided");
    } else {
      line("run", "npm install");
      const install = run("npm", ["install"], { stdio: "inherit" });
      if (!install.ok) {
        line("missing", "npm install failed; run it manually after checking npm/network access");
      }
    }
  } else {
    line("ok", "node dependencies already installed");
  }

  await maybeCloneHermesAgent(hermesAgentSrc, currentEnv);

  console.log("");
  console.log("Running final baseline check...");
  const check = run("node", [path.join(APP_ROOT, "scripts", "init-baseline.mjs")], { stdio: "inherit" });
  if (!check.ok) {
    console.log("");
    console.log("Baseline setup finished, but required dependencies are still unavailable.");
    console.log("Install the dependency shown above, then rerun npm run setup.");
    process.exit(check.status || 1);
  }

  console.log("");
  console.log("Baseline setup complete.");
}

main().catch((error) => {
  console.error(error.stack || error.message);
  process.exit(1);
});
